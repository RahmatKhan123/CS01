/*this program demosntrad the prefix and postfix
increment and decrement operators.*/
#include <iostream>
using namespace std;
int main()
{
  int num = 4;
  cout << num << endl;
  cout << num++ << endl;

  cout << num << endl;
  cout << ++num << endl;
  cout << endl;

  cout << num << endl;
  cout << num-- << endl;
  cout << num << endl;
  cout << --num << endl;
  cout << endl; 


}